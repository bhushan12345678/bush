package gchgcchgc;






public class number {
	
	private int num;
	private String s;
	private char k;
	
	
	
	number(int num, String s,char k){
		this.num=num;
		this.s=s;
		this.k=k;
	}



	public int getNum() {
		return num;
	}



	public void setNum(int num) {
		this.num = num;
	}



	public String getS() {
		return s;
	}



	public void setS(String s) {
		this.s = s;
	}



	public char getK() {
		return k;
	}



	public void setK(char k) {
		this.k = k;
	}
	
	
	
	
	
	
}
